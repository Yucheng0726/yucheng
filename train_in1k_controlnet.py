#创建训练数据集 定义了自定义的ControlNetImageFolder类，并根据提供的参数创建训练数据集
def make_train_dataset(args, accelerator):
    
    
    class ControlNetImageFolder(torchvision.datasets.ImageFolder):
        def __init__(self, root, max_samples=50000, condition='canny', transform=None, target_transform=None, cond_transform=None, *args, **kwargs):
            super().__init__(root=root, transform=transform, target_transform=target_transform)
            self.max_samples = max_samples
            self.condition = condition
            self.cond_transform = cond_transform

            # if imagenet100
            if 'imagenet100' in root:
                import yaml
                with open('data/index_synset.yaml', "r") as f:
                    idx2syn_dict = yaml.safe_load(f)
                syn2idx_dict = {v:k for k,v in idx2syn_dict.items()}
                
                target_transform = {}
                for synset, class_idx in self.class_to_idx.items():
                    target_transform[class_idx] = syn2idx_dict[synset]
                
                print('target_transform', target_transform)
                
                self.target_transform = target_transform
            
            cond_imgs = [s[0].replace('imagenet100', f'imagenet100_{condition}') for s in self.samples]
            
            # sanity check
            checked_samples = []
            checked_cond_imgs = [] 
            for i in range(len(cond_imgs)):
                if not os.path.exists(cond_imgs[i]):
                    continue 
                
                try:
                    img = Image.open(cond_imgs[i])
                except:
                    continue
                checked_samples.append(self.samples[i])
                checked_cond_imgs.append(cond_imgs[i])
            self.imgs = checked_samples
            self.samples = checked_samples
            self.cond_imgs = checked_cond_imgs
            self.targets = [s[1] for s in self.samples]
            
        def __getitem__(self, index):
            """
            Args:
                index (int): Index

            Returns:
                tuple: (sample, target) where target is class_index of the target class.
            """
            path, target = self.samples[index]
            sample = self.loader(path)
            
            # contional sample
            cond_sample = self.loader(self.cond_imgs[index])
        
            if self.transform is not None:
                sample = self.transform(sample)
            if self.cond_transform is not None:
                cond_sample = self.cond_transform(cond_sample)
            if self.target_transform is not None:
                target = self.target_transform[target]

            return sample, cond_sample, target
            
    
    image_transforms = transforms.Compose(
        [
            transforms.Resize(args.resolution, interpolation=transforms.InterpolationMode.BILINEAR),
            transforms.CenterCrop(args.resolution),
            transforms.ToTensor(),
            transforms.Normalize([0.5], [0.5]),
        ]
    )

    conditioning_image_transforms = transforms.Compose(
        [
            transforms.Resize(args.resolution, interpolation=transforms.InterpolationMode.BILINEAR),
            transforms.CenterCrop(args.resolution),
            transforms.ToTensor(),
        ]
    )
    
    train_dataset = ControlNetImageFolder(
        args.train_data_dir,
        max_samples=args.max_samples,
        condition=args.condition,
        transform=image_transforms,
        cond_transform=conditioning_image_transforms,
    )
    return train_dataset


